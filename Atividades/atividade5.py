# Calcular a area de um triangulo
# Formula: b*h / 2

baseTriangulo = int(input("Base: "))
alturaTriangulo = int(input("Altura: "))
areaTriangulo = (baseTriangulo * alturaTriangulo) / 2
print("Base: ", baseTriangulo, "\n"
      "Altura: ", alturaTriangulo, "\n"
      "Area do triangulo: ", areaTriangulo)