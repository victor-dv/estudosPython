num1 = int(input("Numero 1: "))
num2 = int(input("Numero 2: "))

print (f"Multiplicação entre {num1} e {num2} é: ", num1 * num2)
print (f"Divisão entre {num1} e {num2} é: ", num1 / num2)
print (f"Soma entre {num1} e {num2} é: ", num1 + num2)
print (f"Subtração entre {num1} e {num2} é: ", num1 - num2)
 
